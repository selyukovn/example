package request

import (
	"context"
	"example/admin/cfm/internal/domain/cfm"
	assert "github.com/selyukovn/go-wm-assert"
)

// ---------------------------------------------------------------------------------------------------------------------
// Const
// ---------------------------------------------------------------------------------------------------------------------

var ArgsNil = Args{}

// ---------------------------------------------------------------------------------------------------------------------
// Struct
// ---------------------------------------------------------------------------------------------------------------------

type Args struct {
	ctx   context.Context
	cfmId cfm.Id
}

// ---------------------------------------------------------------------------------------------------------------------
// Create
// ---------------------------------------------------------------------------------------------------------------------

// NewArgs
//
// Паникует при нулевых аргументах.
func NewArgs(ctx context.Context, cfmId cfm.Id) Args {
	assert.NotNilDeepMust(ctx)
	assert.FalseMust(cfmId.IsNil())

	return Args{
		ctx:   ctx,
		cfmId: cfmId,
	}
}

// ---------------------------------------------------------------------------------------------------------------------
// State
// ---------------------------------------------------------------------------------------------------------------------

func (a Args) IsNil() bool {
	return a == ArgsNil
}

func (a Args) Ctx() context.Context {
	return a.ctx
}

func (a Args) CfmId() cfm.Id {
	return a.cfmId
}

