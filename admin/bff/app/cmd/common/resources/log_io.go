package resources

import (
	assert "github.com/selyukovn/go-wm-assert"
	"io"
	"os"
)

// ---------------------------------------------------------------------------------------------------------------------
// Struct
// ---------------------------------------------------------------------------------------------------------------------

type LogIo struct {
	file io.WriteCloser
}

// ---------------------------------------------------------------------------------------------------------------------
// Create
// ---------------------------------------------------------------------------------------------------------------------

func NewLogIoFile(pathToFile string) *LogIo {
	assert.Str().NotEmpty().Must(pathToFile)

	file, err := os.OpenFile(pathToFile, os.O_CREATE|os.O_APPEND|os.O_WRONLY, 0666)
	if err != nil {
		panic(err)
	}

	return &LogIo{file: file}
}

// ---------------------------------------------------------------------------------------------------------------------
// Actions
// ---------------------------------------------------------------------------------------------------------------------

func (l *LogIo) Write(b []byte) (int, error) {
	return l.file.Write(b)
}

func (l *LogIo) Close() error {
	return l.file.Close()
}

