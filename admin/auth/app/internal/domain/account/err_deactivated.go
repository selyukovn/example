package account

import (
	"fmt"
	assert "github.com/selyukovn/go-wm-assert"
)

// ---------------------------------------------------------------------------------------------------------------------
// Struct
// ---------------------------------------------------------------------------------------------------------------------

type ErrorDeactivated struct {
	accId Id
}

// ---------------------------------------------------------------------------------------------------------------------
// Create
// ---------------------------------------------------------------------------------------------------------------------

// NewErrorDeactivated
//
// Паникует при нулевых аргументах.
func NewErrorDeactivated(accId Id) ErrorDeactivated {
	assert.FalseMust(accId.IsNil())

	return ErrorDeactivated{accId: accId}
}

// ---------------------------------------------------------------------------------------------------------------------
// State
// ---------------------------------------------------------------------------------------------------------------------

func (e ErrorDeactivated) Error() string {
	return fmt.Sprintf("аккаунт %q деактивирован", e.accId.String())
}

func (e ErrorDeactivated) AccId() Id {
	return e.accId
}

