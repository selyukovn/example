package cfm

import (
	"github.com/selyukovn/go-id/like_uuid"
	"github.com/selyukovn/go-std"
)

// ---------------------------------------------------------------------------------------------------------------------
// Const
// ---------------------------------------------------------------------------------------------------------------------

var IdNil = Id(like_uuid.IdNil)

// ---------------------------------------------------------------------------------------------------------------------
// Struct
// ---------------------------------------------------------------------------------------------------------------------

type Id like_uuid.Id

// ---------------------------------------------------------------------------------------------------------------------
// Create
// ---------------------------------------------------------------------------------------------------------------------

// IdFromString
//
//   - std.ErrorValidation
func IdFromString(value string) (Id, error) {
	id, err := like_uuid.IdFromString(value)

	if err != nil {
		return IdNil, std.NewErrorValidationFf(err.Error())
	}

	return Id(id), nil
}

// ---------------------------------------------------------------------------------------------------------------------
// State
// ---------------------------------------------------------------------------------------------------------------------

func (id Id) IsNil() bool {
	return like_uuid.Id(id).IsNil()
}

func (id Id) String() string {
	return like_uuid.Id(id).String()
}

