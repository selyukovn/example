########################################################################################################################
# DB AUTH
########################################################################################################################

CREATE DATABASE auth;
USE auth;

# Теоретически, может понадобиться еще и TRUNCATE, но он предоставляется только вместе с DROP-привилегией,
# поэтому, если такое разрешение и понадобится, то лучше его предоставлять для отдельных таблиц.
# CREATE TEMPORARY TABLES -- т.к. применение достаточно редкое, пусть тоже будет запрещено по умолчанию.
GRANT SELECT, INSERT, UPDATE, DELETE ON auth.* TO 'docker_app_user'@'172.16.0.0/255.240.0.0';

GRANT ALL ON auth.* TO 'docker_migration_user'@'172.16.0.0/255.240.0.0';

FLUSH PRIVILEGES;

